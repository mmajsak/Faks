<h2>São Paulo</h2>
<h3>When was the track built?</h3>
<p>Building work began on what ended up being called the Autodromo Jose Carlos Pace – but what is more commonly referred to as Interlagos – all the way back in 1938. 
    The track designers took their inspiration from three main circuits: Brooklands in the UK, Roosevelt Raceway in the USA and Montlhery in France.</p>

<h3>When was its first Grand Prix?</h3>
<p>Buoyed by the success of Brazil’s Emerson Fittipaldi, Formula 1 first jetted into Interlagos for a world championship race in 1973. 
    Fans were treated to a home win in the first three Brazilian Grands Prix, with Fittipaldi victorious in 1973 and 1974, while Carlos Pace won in 1975.</p>

<h3>What’s the circuit like?</h3>
<p>Like many pre-World War II tracks, Interlagos features banked corners, with the drivers beginning their lap on a sort of half oval – in fact, between 1957 and the track’s 
    return to the F1 calendar in 1990, Interlagos could be run as a giant oval. After wiggling through the Senna S and down to Turn 4, the drivers then go through a snaking 
    in-field section with some challenging camber changes, before slinging back up the hill and through the banked final turn.</p>

<h3>Why go?</h3>
<p>A carnival atmosphere really does dominate in Brazil, and watching Formula 1 cars alongside the locals is something every F1 fan should experience. True, it doesn’t look 
    like there’ll be a local driver to cheer on any time soon, but that won’t stop the party at Interlagos.</p>

<h3>Where is the best place to watch?</h3>
<p>Because of the bowl-like nature of the track, a place in Grandstand A on the banked entry to the start-finish straight will give you a double whammy of views, allowing you 
    to see the cars winding through the infield section and then passing underneath you. On the other end of the straight, Grandstand M will put you on top of the first corner 
